class OfferedFood {

  final DateTime date;
  final String name;
  final String allergens;
  final int numberOfServings;
  final String packaging;
  final String consumeBy;

  OfferedFood({
    required this.date,
    required this.name,
    required this.allergens,
    required this.numberOfServings,
    required this.packaging,
    required this.consumeBy
  });

}